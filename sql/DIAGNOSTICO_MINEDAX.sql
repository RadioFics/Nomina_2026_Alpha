PRINT '════ 6. EMPLEADOS REGISTRADOS (Muestra) ════'
SELECT TOP 10
  f.COD_FUNCI,
  t.NUM_IDEN AS CEDULA,
  t.NOM_COMP AS NOMBRE,
  f.ACT_ESTA,
  t.ACT_ESTA AS ESTADO_TERCERO
FROM GN_FUNCI f
INNER JOIN GN_TERCE t ON t.COD_TERC = f.COD_TERC
WHERE f.ACT_ESTA = 'A'
  AND t.ACT_ESTA = 'A'
ORDER BY t.NOM_COMP;

