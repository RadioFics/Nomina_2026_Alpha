PRINT '════ 1. VERIFICAR TABLAS PRINCIPALES ════'
SELECT
  TABLE_NAME,
  CASE
    WHEN TABLE_NAME = 'NO_NOVED' THEN 'Novedades/Permisos'
    WHEN TABLE_NAME = 'NO_AUSEN' THEN 'Ausencias/Vacaciones'
    WHEN TABLE_NAME = 'NO_CONCE' THEN 'Conceptos de Nómina'
    WHEN TABLE_NAME = 'NO_PERIOD' THEN 'Períodos de Nómina'
    WHEN TABLE_NAME = 'GN_FUNCI' THEN 'Empleados'
    WHEN TABLE_NAME = 'GN_TERCE' THEN 'Terceros (Personas)'
    ELSE 'Otra'
  END AS Descripcion
FROM INFORMATION_SCHEMA.TABLES
WHERE TABLE_NAME IN ('NO_NOVED', 'NO_AUSEN', 'NO_CONCE', 'NO_PERIOD', 'GN_FUNCI', 'GN_TERCE')
ORDER BY TABLE_NAME;

PRINT ''
