IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME = 'NO_CONCE')
BEGIN
  SELECT TOP 10
    COD_CONC,
    NOM_CONC,
    TIP_CONC,
    IND_ACT,
    ACT_ESTA
  FROM NO_CONCE
  WHERE NOM_CONC LIKE '%PERMISO%'
     OR NOM_CONC LIKE '%VACACIO%'
  ORDER BY NOM_CONC;
END
ELSE
BEGIN
  PRINT 'ERROR: Tabla NO_CONCE NO EXISTE'
