PRINT ''
PRINT '════ 5. PERÍODO ACTUAL ACTIVO ════'
IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME = 'NO_PERIOD')
BEGIN
  SELECT TOP 1
    COD_PERIOD,
    PER_ANO,
    PER_MES,
    PER_QNA,
    PER_FINI,
    PER_FFIN,
    PER_EST,
    ACT_ESTA
  FROM NO_PERIOD
  WHERE PER_EST = 'A'
    AND ACT_ESTA = 'A'
    AND CONVERT(date, GETDATE()) BETWEEN PER_FINI AND PER_FFIN
  ORDER BY PER_FINI DESC;
END
ELSE
BEGIN
  PRINT 'ERROR: Tabla NO_PERIOD NO EXISTE'
END;

